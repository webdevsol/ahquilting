  <div style="position:relative;z-index:101">
  The Lancaster Diamond Sampler was made by one anonymous person, referred to as Fanny's friend. Fanny S. Bucher and her friend were sharing fabric and block designs as they made their unique diamond-shaped blocks into sampler quilts quilts meant for teaching and learning the craft. As you follow along with Fanny's friend, learn or re-learn the skills needed for making your own quilt.
<br><br>
Supplemented by over 500 instructional images and diagrams of quilts, blocks, and needlework, the basics of fabric selection, grain, placement, and seaming are demonstrated. You will see Fanny's friend's skills develop along with your own from simple piecing to the elements of patchwork, complex miniatures, and applique. 
<br><br>
These skills are enhanced when practiced with a group, so share the patterns provided with other quilters to learn and create your own sampler together!&nbsp;</div>
